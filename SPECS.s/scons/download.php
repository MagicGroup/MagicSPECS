<h3> scons-local Packages </h3>
<p>
The <span class="sconslogo">scons-local</span> packages contain versions of
<span class="sconslogo">SCons</span> that you can execute standalone,
out of a local directory.
They are intended to be dropped in to and shipped with packages of other
software that you want to build with <span class="sconslogo">SCons</span>,
but for which you don't want to have to require that your users install
<span class="sconslogo">SCons</span>:
